<?php
/********************************************************************************************************************************/
// App default
/********************************************************************************************************************************/
class app_default
{
/********************************************************************************************************************************/
// Constructor
	function __construct()
	{
	}
	
/********************************************************************************************************************************/
// Execute
	function execute()
	{
		// Check for requested action
		$app_action = isset($_REQUEST['ax']) ? $_REQUEST['ax'] : false;
		
		// Execute request action
		switch($app_action)
		{
			case 'startup':
				$this->app_view();
			break;
			
			default:
				$this->default_action();
			break;
		}
	}
/********************************************************************************************************************************/
// ACTIONS
/********************************************************************************************************************************/

/********************************************************************************************************************************/	
// Default action
	function default_action()
	{
		echo '<h1>DEFAULT</h1>';
	}

/********************************************************************************************************************************/	
// Action 2	
	function app_view()
	{
		echo '<h1>APP VIEW</h1>';
	}
	
/********************************************************************************************************************************/
// END CLASS
/********************************************************************************************************************************/
}
/********************************************************************************************************************************/
// END FILE
/********************************************************************************************************************************/
?>